    private void btnEnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnregistrerActionPerformed
        CaisseDAO cdao = new CaisseDAO();
        String mont = jTextField2.getText();
        String libelle =jTextField1.getText();
        String type = jComboBox1.getSelectedItem().toString();
        if (mont.equals("") || libelle.equals("")|| type.equals("")) {
            new Message().affiche("Veuillez renseigner tous les champs ",Message.ALERT);
        } 
        else if(modifier==false) {
            Caisse css = new Caisse(user.getIdu(),type, libelle,Float.parseFloat(mont));
            cdao.inserer(css);
            vider();
            liste_depense();
            montant=0;mEntre=0;mSortie=0;
            caisse();
            frameEnregistrer.setVisible(false);
        }
        else if(modifier==true) {
            int index = tb_caisse.getSelectedRow();
             int id = (int)tb_caisse.getValueAt(index,0);
            Caisse css = new Caisse(user.getIdu(),type, libelle,Float.parseFloat(mont));
            cdao.modifier(css, id);
            vider();
            liste_depense();
            montant=0;mEntre=0;mSortie=0;
            caisse();
            frameEnregistrer.setVisible(false);
        }
    }//GEN-LAST:event_btnEnregistrerActionPerformed

