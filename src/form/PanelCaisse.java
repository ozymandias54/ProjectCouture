private void liste_depense() {
        CaisseDAO pdao = new CaisseDAO();
        List<Caisse> css = pdao.liste();
        
        
        for (int i = 0; i < css.size(); i++) {
            UtilisateurDAO udao=new UtilisateurDAO();
            Utilisateur ut=udao.recherche(css.get(i).getIdu());
            String login=ut.getLogin();
            ((DefaultTableModel) tb_caisse.getModel()).addRow(new Object[]{
                  css.get(i).getIdCaisse(),
                  css.get(i).getType(),
                  css.get(i).getLibelle(),
                  css.get(i).getMontant(),
                  css.get(i).getDate_operation(),
                  login
            });
            }
    }
public void vider() {
        DefaultTableModel model = (DefaultTableModel) tb_caisse.getModel();
        int n = model.getRowCount();
        for (int i = n - 1; i >= 0; --i) {
            model.removeRow(i);
        }
    }
private void liste_entre() {
        CaisseDAO pdao = new CaisseDAO();
        List<Caisse> css = pdao.liste();
        UtilisateurDAO udao=new UtilisateurDAO();
        for (int i = 0; i < css.size(); i++) {
            Utilisateur u=udao.recherche(css.get(i).getIdu());
            if(css.get(i).getType().equals("entr√©e")){
            ((DefaultTableModel) tb_caisse.getModel()).addRow(new Object[]{
                  css.get(i).getIdCaisse(),
                  css.get(i).getType(),
                  css.get(i).getLibelle(),
                  css.get(i).getMontant(),
                  css.get(i).getDate_operation(),
                  u.getLogin()
            });
            }
        }
    }
