    private void tb_utilisateurMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_utilisateurMouseClicked
        int index = tb_utilisateur.getSelectedRow();
        if (index!=-1){
          int id = (int)tb_utilisateur.getValueAt(index,0);
            UtilisateurDAO udao = new UtilisateurDAO();
            Utilisateur ut = udao.recherche(id);
            jTextField2.setText(ut.getLogin());
            jFormattedTextField1.setText(ut.getPassword());
            jComboBox1.setSelectedItem(ut.getProfil());
            PersonnelDAO pdao = new PersonnelDAO();
            Personnel personnel=pdao.recherche(ut.getIdp());
            jComboBox2.setSelectedItem(personnel.getIdp()+" "+personnel.getNom()+" "+personnel.getPrenom());
        }
    }//GEN-LAST:event_tb_utilisateurMouseClicked

