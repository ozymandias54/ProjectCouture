    private void btnEnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnregistrerActionPerformed
        UtilisateurDAO udao = new UtilisateurDAO();
        String login = jTextField2.getText();
        String password =jFormattedTextField1.getText();
        String profil = jComboBox1.getSelectedItem().toString();
        int indexfin=jComboBox2.getSelectedItem().toString().indexOf(" ");
        String idp=jComboBox2.getSelectedItem().toString().substring(0, indexfin);
        if (login.equals("") || password.equals("")) {
            new Message().affiche("Veuillez renseigner tous les champs ",Message.ALERT);
        } 
        else if(modifier==false) {
            Utilisateur ut = new Utilisateur(login, password,profil,Integer.parseInt(idp));
            udao.inserer(ut);
            vider();
            liste_utilisateur();
            FrameEnregistrer.setVisible(false);
        }
        else if(modifier==true) {
            int index = tb_utilisateur.getSelectedRow();
             int id = (int)tb_utilisateur.getValueAt(index,0);
            Utilisateur ut = new Utilisateur(login, password,profil,Integer.parseInt(idp));
            udao.modifier(ut, id);
            vider();
            liste_utilisateur();
            FrameEnregistrer.setVisible(false);
        }
    }//GEN-LAST:event_btnEnregistrerActionPerformed

