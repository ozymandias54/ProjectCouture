    private void btnEnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnregistrerActionPerformed
        PersonnelDAO pdao = new PersonnelDAO();
        String CIN=jTextField1.getText();
        String nom=jTextField2.getText();
        String prenom=jTextField3.getText();
        String poste=jTextField4.getText();
        String telephone=jTextField5.getText();
        Date date_naissance=jDateChooser1.getDate();
        Date date_embauche=jDateChooser2.getDate();
        
        boolean sexe=true;
        if(jRadioButton1.isSelected()==true)
            sexe=true;
        else if(jRadioButton2.isSelected()==true)
            sexe=false;
        String salaire=jTextField8.getText();
        if(CIN.equals("")||nom.equals("")||prenom.equals("")||poste.equals("")||telephone.equals("") ||salaire.equals("")){
            new Message().affiche("Veuillez renseigner tous les champs ",Message.INFO);
        }
        else if(modifier==false) {
            Personnel p = new Personnel(CIN, nom, prenom, poste, telephone, date_naissance, date_embauche, sexe,Double.parseDouble(salaire));
            pdao.inserer(p);
            vider();
            liste_personnel();
            FrameEnregistrer.setVisible(false);
        }
        else if(modifier==true){
            int index = tb_personnel.getSelectedRow();
             int id = (int)tb_personnel.getValueAt(index,0);
            Personnel ut = new Personnel(CIN, nom, prenom, poste, telephone, date_naissance, date_embauche, sexe,Double.parseDouble(salaire));
            pdao.modifier(ut, id);
            vider();
            liste_personnel();
            FrameEnregistrer.setVisible(false);
        }  
    }//GEN-LAST:event_btnEnregistrerActionPerformed

